    static let LARGE_SIZE = UIFont.systemFont(ofSize: 20)
    // 文字サイズ（中）
    static let MIDDLE_SIZE = UIFont.systemFont(ofSize: 16)
    // 文字さいざ（小）
    static let SMALL_SIZE = UIFont.systemFont(ofSize: 14)
}
