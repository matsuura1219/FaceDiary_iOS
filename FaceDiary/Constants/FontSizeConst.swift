 */

class FontSizeConst {
    
    //　文字サイズ（大大大）
    static let XX_LARGE_SIZE = UIFont.systemFont(ofSize: 30)
    //　文字サイズ（大大）
    static let X_LARGE_SIZE = UIFont.systemFont(ofSize: 24)
